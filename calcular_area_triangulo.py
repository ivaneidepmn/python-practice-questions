# 1ª Tentativa

def calcular_area_triangulo_1(base, altura):
    """
    Calcula a área de um triângulo dado a base e a altura.

    Parâmetros:
    base (float): A base do triângulo.
    altura (float): A altura do triângulo.

    Retorna:
    float: A área calculada do triângulo.
    """
    area = 0.5 * base * altura  # Fórmula para calcular a área do triângulo
    return area

# Exemplo de uso da função 1
base1 = 5.0  # Exemplo de valor da base do triângulo
altura1 = 8.0  # Exemplo de valor da altura do triângulo

area_do_triangulo_1 = calcular_area_triangulo_1(base1, altura1)
print(f"Tentativa 1 - A área do triângulo com base {base1} e altura {altura1} é: {area_do_triangulo_1}")

